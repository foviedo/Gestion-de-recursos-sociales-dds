package domain;

public class infoBase {
String nombreFicticio;
String descripcion;
String nombre;
Integer id;
Integer id_organizacion;
Integer id_juridica;
public infoBase () {
	
}

public String getNombreFicticio() {
	return nombreFicticio;
}
public void setNombreFicticio(String nombreFicticio) {
	this.nombreFicticio = nombreFicticio;
}
public String getDescripcion() {
	return descripcion;
}
public void setDescripcion(String descripcion) {
	this.descripcion = descripcion;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre;
}
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public Integer getId_organizacion() {
	return id_organizacion;
}
public void setId_organizacion(Integer id_organizacion) {
	this.id_organizacion = id_organizacion;
}
public Integer getId_juridica() {
	return id_juridica;
}
public void setId_juridica(Integer id_juridica) {
	this.id_juridica = id_juridica;
}
}
